	<h2>About MUN</h2>
	<p class="desc">MUN or Model United Nations are conferences that are primarily academic simulations of the working of the United Nations and incorporates conformist United Nations rules, procedures and workflow between International delegations, Executive Board and the International Press while involving qualified, upper crust parliamentary debaters from all academic backgrounds, undergraduate and postgraduate. At a MUN, one is bound to witness exhilarating debate on a particular agenda in parliamentary form within parliamentary walls.</p>
	<p class="desc">Model United Nations differ vastly from standard debating competitions due to its vastness, and its sophisticated nature. An MUN reaches out to, and involves far more competitors than a large scale debate due to its parliamentary nature which may include a whole or a part committee of 40-50 delegates, sometimes even 90 delegates debating together. </p>
	<br/>
	<h2>About BIT MUN</h2>
	<p class="desc">At BITMUN, there is a Secretariat, headed by a Secretary General and an Executive Board who shall oversee debate. Adhering to strict United Nations hierarchical structure, all members of Media are headed by a Director of International Press who will supervise World Press, and all Under Secretary Generals are supervised by a Director General, within the Secretariat. International Press Delegation comprises of a team of around 20 journalists and photographers selected after diligent research and training by the BITMUN Secretariat, and the Executive Board comprises of experienced MUNers. BITMUN looks forward to an exuberant conference with a very high quality of debate.  </p>

	<p class="desc">BIT MUN came into existence in 2013 via BIT MUN’13 held at Birla Institute of Technology, Mesra, (Jaipur Extension Campus). The first edition of BIT MUN was an immense success with overwhelming responses of people from all over the country. </p>

