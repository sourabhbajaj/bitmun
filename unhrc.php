	<h2>UNHRC</h2>
	<p class="desc">
	The Human Rights Council is an inter-governmental body within the United Nations system responsible for strengthening the promotion and protection of human rights around the globe and for addressing situations of human rights violations and make recommendations on them. It has the ability to discuss all thematic human rights issues and situations that require its attention throughout the year. It meets at the UN Office at Geneva.
	</p>
	<p class="desc">The Council was created by the United Nations General Assembly on 15 March 2006 by resolution 60/251. Its first session took place from 19 to 30 June 2006. One year later, the Council adopted its "Institution-building package" to guide its work and set up its procedures and mechanisms.</p>
	<p class="desc">Among them were the Universal Periodic Review mechanism which serves to assess the human rights situations in all United Nations Member States, the Advisory Committee which serves as the Council’s “think tank” providing it with expertise and advice on thematic human rights issues and the Complaint Procedure which allows individuals and organizations to bring human rights violations to the attention of the Council.</p>
